    def cadastrar_usuario(self):
        usuario = self.Entry_usuario.get()
        senha = self.Entry_senha.get()
        nome = self.Entry_nome.get()
        email = self.Entry_email.get()

        query_insert = "INSERT INTO cadastro (login_usuario, senha_usuario, nome_usuario, email_usuario) VALUES (%s, %s, %s, %s)"
        valores_insert = (usuario, senha, nome, email)

        try:
            self.cursor.execute(query_insert, valores_insert)
            self.conexao.commit()
            print("Usuário cadastrado com sucesso!")
            self.limpa_tela()
        except Error as e:
            print("Erro ao cadastrar usuário:", e)

    def verificar_login(self):
        usuario = self.Entry1_usuario.get()
        senha = self.Entry2_senha.get()

        query_select = "SELECT * FROM cadastro WHERE login_usuario=%s AND senha_usuario=%s"
        valores_select = (usuario, senha)

        try:
            self.cursor.execute(query_select, valores_select)
            rows = self.cursor.fetchall()

            if len(rows) > 0:
                print("Login realizado com sucesso!")
                self.root.destroy()
                Toplevel1()
            else:
                print("Usuário ou senha inválidos.")

        except Error as e:
            print("Erro ao verificar login:", e)
        
