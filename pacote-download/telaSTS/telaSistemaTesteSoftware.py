    def inserir_artefatos_entrada(self):
        self.arquivo = self.entry_artefato_entrada.get()  # Obtém o caminho do arquivo da Entry
        self.artefatos_entrada = ""
    
        # Lê o conteúdo do arquivo
        with open(self.arquivo, 'r') as file:
            self.artefatos_entrada = file.read()

        # Conecta ao banco de dados MySQL
        self.conecta_bd()
        # Executa a query para inserir o conteúdo do arquivo no banco de dados
        self.cursor.execute("INSERT INTO artefato_entrada (conteudo_entrada) VALUES (%s)", (self.artefatos_entrada,))
    
        self.desconecta_bd

